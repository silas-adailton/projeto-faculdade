    private void jButtonRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegistrarActionPerformed
        Presenca p = new Presenca();
        Aluno a = new Aluno();
        PresencaDao pdao = new PresencaDao();
        
        if(pdao.verificaAluno()){
              try {
             if (jTextFieldCodAluno.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Preencha o campo código");
        } else {
            a.setCodAluno(Integer.parseInt(jTextFieldCodAluno.getText()));
            p.setData(LocalDate.now());
            pdao.inserirPresenca(p, a);
        }
                
        } catch (NumberFormatException ne) {
            
            JOptionPane.showMessageDialog(rootPane,"O campo Codigo aluno só aceita números. \n" + ne.getMessage());
        }
        }else{
            JOptionPane.showMessageDialog(null, "Código invalido!");
        }


    }//GEN-LAST:event_jButtonRegistrarActionPerformed

