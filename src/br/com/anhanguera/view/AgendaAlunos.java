    public final void ConsultarAgenda() {
        Atividade ativ = new Atividade();
        AtividadeDao ativDao = new AtividadeDao();

        ativ.setData(LocalDate.now());
        // String filtro = "data";//Filtro para seleção no banco
      /*Lista de atividade que recebe o metodo selectAtividades() passando por parametro
         um objeto atividade e o filtro para seleção no banco*/
        ArrayList<Atividade> listaAtividade = ativDao.consultarAgendaAluno();

        //Objeto DateTimeFormatter para formatação de datas
        DateTimeFormatter dt = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        DefaultTableModel modelo = (DefaultTableModel) jTableAgendaAlunos.getModel();
        modelo.setRowCount(0);
        for (Atividade a : listaAtividade) {
            Object con[] = {
                a.getData().format(dt),
                a.getHoraInicio(),
                a.getHoraFim(),
                a.getNome()};

            modelo.addRow(con);

        }

    }

