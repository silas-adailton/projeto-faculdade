    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        Aluno a = new Aluno();
        AlunoDao adao = new AlunoDao();
        
        
        a.setCodAluno(Integer.parseInt(jTextFieldCodigoAluno.getText()));
        a.setCodAtividade(Integer.parseInt(jTextFieldCodAtividade.getText()));
        a.setNome(jTextFieldNome.getText());
        DateTimeFormatter dtFormatada = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        LocalDate dt = LocalDate.parse(jFormattedTextFieldDataNasc.getText(), dtFormatada);
        a.setDataNasc(dt); 
        a.setIdade(Integer.parseInt(jTextFieldIdade.getText()));
        //Endere√ßo
        a.setRua(jTextFieldRua.getText());
        a.setBairro(jTextFieldBairro.getText());
        a.setCidade(jTextFieldCidade.getText());
        a.setUf(jComboBoxUf.getSelectedItem().toString().trim());
        //Telefone
        a.setCel(jTextFieldCelular.getText());
        a.setTel(jTextFieldTelefone.getText());              
        a.setRg(jTextFieldRg.getText());
        a.setCpf(jTextFieldCpf.getText());      

        adao.cadastrarAluno(a);
    }//GEN-LAST:event_jButtonSalvarActionPerformed


